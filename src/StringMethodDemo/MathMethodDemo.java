package StringMethodDemo;
public class MathMethodDemo {
	public static void main(String[] args) {
		System.out.println("absolute  value of -1 is "+Math.abs(-1));
		System.out.println("round value of 24.7 is "+Math.round(24.7));
		System.out.println("ceil value of 33.8 is "+Math.ceil(33.8));
		System.out.println("floor value of 33.8 is "+Math.floor(33.8));
		System.out.println("exp value of 2 is "+Math.exp(2));
		System.out.println("Max between 2 and 7 is "+Math.max(2, 7));
		System.out.println("min between 33 and 99 is "+Math.min(33,99));
	}

}
